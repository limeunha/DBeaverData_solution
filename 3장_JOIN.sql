-- 예제3-2. A학원과 B학원 테이블 조인하여 조회하기
SELECT * 
FROM ACADEMY_A T1
JOIN ACADEMY_B T2
ON (T1.STUDENT_NO = T2.STUDENT_NO);

-- 예제1. A학원과 B학원 테이블 OUTER 조인하여 조회하기
SELECT T1.STUDENT_NO, T1.STUDENT_NAME, T2.STUDENT_NO, T2.STUDENT_NAME
FROM ACADEMY_A T1
FULL OUTER JOIN ACADEMY_B T2
ON (T1.STUDENT_NO = T2.STUDENT_NO)
ORDER BY T1.STUDENT_NO;

-- 예제1. A학원과 B학원 테이블 LEFT 조인하여 조회하기
SELECT * 
FROM ACADEMY_A T1
LEFT JOIN ACADEMY_B T2
ON (T1.STUDENT_NO = T2.STUDENT_NO)
ORDER BY T1.STUDENT_NO;

-- 예제1. A학원과 B학원 테이블 RIGHT 조인하여 조회하기
SELECT * 
FROM ACADEMY_A T1
RIGHT JOIN ACADEMY_B T2
ON (T1.STUDENT_NO = T2.STUDENT_NO)
ORDER BY T1.STUDENT_NO;

-- 예제1. A학원과 B학원 테이블 NATURAL 조인 하여 조회하기
SELECT * 
FROM ACADEMY_A T1
