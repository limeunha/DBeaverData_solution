-- 예제3-1. A학원과 B학원 테이블 조인하여 조회하기
SELECT * 
FROM ACADEMY_A T1, ACADEMY_B T2
WHERE T1.STUDENT_NO = T2.STUDENT_NO;

SELECT T1.STUDENT_NAME, T1.STUDENT_NO 
FROM ACADEMY_A T1, ACADEMY_B T2
WHERE T1.STUDENT_NO = T2.STUDENT_NO;

--NONE EQUI
